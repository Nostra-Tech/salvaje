function _norm(str) {
  return (str || '')
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

const _normCache = new Map();
for (const [k, v] of Object.entries(worldCupBadges)) {
  _normCache.set(_norm(k), v);
}
for (const [alias, canonical] of Object.entries(WC_NAME_ALIASES)) {
  const url = worldCupBadges[canonical];
  if (url) _normCache.set(_norm(alias), url);
}

/**
 * Resuelve el escudo de una selección desde el mapa curado.
 * @param {string} teamName
 * @returns {string|null}
 */
export function resolveNationalTeamBadge(teamName) {
  if (!teamName) return null;
  const norm = _norm(teamName);
  if (_normCache.has(norm)) return _normCache.get(norm);
  for (const [key, url] of _normCache) {
    if (key.length >= 4 && (norm.includes(key) || key.includes(norm))) return url;
  }
  return null;
}

/**
 * Devuelve el escudo de un equipo, con fallback garantizado (nunca null).
 * @param {string} teamName
 * @returns {string}
 */
export function getBadge(teamName) {
  return resolveNationalTeamBadge(teamName) || WC_FALLBACK_BADGE;
}
